#!/usr/bin/env python3
"""
GPU Dummy Model for Data Loading Benchmarks
Simulates GPU compute delay without requiring a real GPU.
Reads delay parameters from gpu_delays.json (generated by build_delay_config.py).

Usage:
    model = DummyModel(gpu="RTXA6000", model_name="resnet50")
    output = model(batch)  # sleeps ~573ms, returns fake logits
"""
import torch
import random
import time
import json
import os

# Load delay config once at module level
_CONFIG_PATH = os.path.join(os.path.dirname(__file__), "gpu_delays.json")
try:
    with open(_CONFIG_PATH, 'r') as f:
        DELAY_CONFIG = json.load(f)
except FileNotFoundError:
    print(f"Warning: {_CONFIG_PATH} not found. Run build_delay_config.py first.")
    DELAY_CONFIG = {}


class DummyModel:
    """
    Simulates GPU compute delay for data loading benchmarks.

    Args:
        gpu: GPU name (e.g., "RTXA6000", "H100", "A100-80GB")
        model_name: Model name (e.g., "resnet50", "vit_b_16")
        num_gpus: Number of GPUs (adjusts throughput, not delay per batch)
        input_size: Expected input image size (default 224)
        num_classes: Number of output classes (default 1000 for ImageNet)

    Example:
        model = DummyModel(gpu="RTXA6000", model_name="resnet50")
        fake_batch = torch.randn(32, 3, 224, 224)
        output = model(fake_batch)  # sleeps ~573ms, returns fake logits
    """

    def __init__(self, gpu="RTXA6000", model_name="resnet50", num_gpus=1,
                 input_size=224, num_classes=1000):
        self.gpu = gpu
        self.model_name = model_name
        self.num_gpus = num_gpus
        self.input_size = input_size
        self.num_classes = num_classes

        # Look up delay from config
        if gpu in DELAY_CONFIG and model_name in DELAY_CONFIG[gpu]["models"]:
            params = DELAY_CONFIG[gpu]["models"][model_name]
            self.delay = params["mean_ms"] / 1000.0  # ms → seconds
            self.std = params["std_ms"] / 1000.0
        elif gpu in DELAY_CONFIG and DELAY_CONFIG[gpu]["default_mean_ms"]:
            # Use GPU default if specific model not found
            self.delay = DELAY_CONFIG[gpu]["default_mean_ms"] / 1000.0
            self.std = DELAY_CONFIG[gpu]["default_std_ms"] / 1000.0
            print(f"Warning: {model_name} not found for {gpu}, using GPU default ({self.delay*1000:.1f}ms)")
        else:
            # Fallback: use RTXA6000/resnet50 as default
            print(f"Warning: {gpu}/{model_name} not in config. Using fallback delay (500ms).")
            self.delay = 0.5
            self.std = 0.0

        print(f"DummyModel: {gpu}/{model_name}, delay={self.delay*1000:.1f}ms ± {self.std*1000:.1f}ms, GPUs={num_gpus}")

    def __call__(self, batch):
        """
        Simulate forward pass.

        Args:
            batch: Tensor of shape (batch_size, 3, input_size, input_size)

        Returns:
            Fake logits of shape (batch_size, num_classes)
        """
        # Calculate delay: variable if std > 0, constant if std = 0
        if self.std > 0:
            current_delay = max(0.0, random.gauss(self.delay, self.std))
        else:
            current_delay = self.delay

        # Simulate GPU compute
        time.sleep(current_delay)

        # Return fake outputs (like a real model would)
        return torch.randn(batch.size(0), self.num_classes)

    def info(self):
        """Return configuration summary."""
        return {
            "gpu": self.gpu,
            "model": self.model_name,
            "num_gpus": self.num_gpus,
            "delay_ms": round(self.delay * 1000, 2),
            "std_ms": round(self.std * 1000, 2),
        }


# ============================================================
# Quick test
# ============================================================
if __name__ == "__main__":
    print("=" * 50)
    print("Testing DummyModel")
    print("=" * 50)

    # Test with known GPU/model
    model1 = DummyModel(gpu="RTXA6000", model_name="resnet50")
    batch = torch.randn(32, 3, 224, 224)
    start = time.time()
    output = model1(batch)
    elapsed = time.time() - start
    print(f"  Input:  {batch.shape} → Output: {output.shape}")
    print(f"  Delay:  {elapsed*1000:.1f}ms (expected ~{model1.delay*1000:.0f}ms)")

    # Test fallback
    print()
    model2 = DummyModel(gpu="FakeGPU", model_name="fakemodel")
    print(f"  Info: {model2.info()}")

    # Test with num_gpus
    print()
    model3 = DummyModel(gpu="H100", model_name="resnet50", num_gpus=4)
    print(f"  Info: {model3.info()}")

    print()
    print("Available GPUs:", list(DELAY_CONFIG.keys()))
